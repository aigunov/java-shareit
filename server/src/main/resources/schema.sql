CREATE TABLE IF NOT EXISTS PUBLIC.users
(
    id    INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name  VARCHAR(255) NOT NULL,
    email VARCHAR(255) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS PUBLIC.request
(
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    description  VARCHAR(255) NOT NULL,
    requester_id INTEGER      NOT NULL REFERENCES users (id),
    created      TIMESTAMP    NOT NULL
);

CREATE TABLE IF NOT EXISTS PUBLIC.items
(
    id           INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name         VARCHAR(255) NOT NULL,
    description  TEXT         NOT NULL,
    is_available BOOLEAN      NOT NULL,
    owner_id     INTEGER      NOT NULL REFERENCES users (id) ON DELETE CASCADE ON UPDATE CASCADE,
    request_id   INTEGER REFERENCES request (id)
);

CREATE TABLE IF NOT EXISTS PUBLIC.comment
(
    id            INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    text          VARCHAR NOT NULL,
    item_id       INTEGER NOT NULL REFERENCES items (id) ON DELETE CASCADE ON UPDATE RESTRICT,
    author_id     INTEGER NOT NULL REFERENCES users (id) ON DELETE CASCADE ON UPDATE CASCADE,
    creation_date TIMESTAMP WITHOUT TIME ZONE
);

CREATE TABLE IF NOT EXISTS PUBLIC.bookings
(
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    booker_id  BIGINT       NOT NULL REFERENCES users (id) ON DELETE CASCADE ON UPDATE CASCADE,
    item_id    BIGINT       NOT NULL REFERENCES items (id) ON DELETE CASCADE,
    start_date TIMESTAMP WITHOUT TIME ZONE,
    end_date   TIMESTAMP WITHOUT TIME ZONE,
    status     VARCHAR(255) NOT NULL
);